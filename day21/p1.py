ARROW_PATH = {
  "A": {"A": "", "^": "<", "v": "<v", "<": "v<<", ">": "v"},
  "^": {"A": ">", "^": "", "v": "v", "<": "v<", ">": "v>"},
  "v": {"A": "^>", "^": "^", "v": "", "<": "<", ">": ">"},
  "<": {"A": ">>^", "^": ">^", "v": ">", "<": "", ">": ">>"},
  ">": {"A": "^", "^": "<^", "v": "<", "<": "<<", ">": ""}
}


def main() -> None:
  keys = open(sys.argv[1]).read().splitlines()
  complexity = 0

  for key in keys:
    # numeric -> directional #1
    prev = "A"
    num_to_dir1_press = ""

    for now in key:
      num_to_dir1_press = num_to_dir1_press + NUMPAD_PATH[prev][now] + "A"
      prev = now

    # directional #1 -> directional #2
    prev = "A"
    dir1_to_dir2_press = ""

    for now in num_to_dir1_press:
      dir1_to_dir2_press = dir1_to_dir2_press + ARROW_PATH[prev][now] + "A"
      prev = now

    # directional #2 -> directional #3
    prev = "A"
    dir2_to_dir3_press = ""

    for now in dir1_to_dir2_press:
      dir2_to_dir3_press = dir2_to_dir3_press + ARROW_PATH[prev][now] + "A"
      prev = now

    complexity += len(dir2_to_dir3_press) * int(key[:-1])

  print(complexity)


if __name__ == "__main__":
  main()